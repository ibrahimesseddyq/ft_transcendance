import { ScanFace } from 'lucide-react';
import { useState, useRef, ChangeEvent, KeyboardEvent } from 'react';

interface InputFieldProps {
    val: string;
    onChange: (value: string) => void;
    onKeyDown: (e: KeyboardEvent<HTMLInputElement>) => void;
    inputRef: (el: HTMLInputElement | null) => void;
}

const InputField = ({ val, onChange, onKeyDown, inputRef }: InputFieldProps) => {
    return (
        <input
            ref={inputRef}
            onChange={(e) => onChange(e.target.value)}
            onKeyDown={onKeyDown}
            value={val}
            type="text"
            maxLength={1}
            inputMode="numeric"
            pattern="[0-9]*"
            required
            autoComplete="one-time-code"
            className="h-10 w-10 md:h-12 md:w-12 text-xl font-bold text-black hover:shadow-lg focus:outline-[#5F88B8]
                       bg-gray-100 border rounded-lg text-center transition-all"
        />
    );
};

export function OTPpage() {

    const [otp, setOtp] = useState<string[]>(new Array(6).fill(""));
    const inputRefs = useRef<(HTMLInputElement | null)[]>([]);

    const handleChange = (value: string, index: number) => {

        if (value !== "" && !/^\d+$/.test(value))
            return;

        const newOtp = [...otp];
        newOtp[index] = value.substring(value.length - 1);
        setOtp(newOtp);

        if (value && index < 5) {
            inputRefs.current[index + 1]?.focus();
        }
    };


    const handleKeyDown = (e: KeyboardEvent<HTMLInputElement>, index: number) => {
        if (e.key === "Backspace" && !otp[index] && index > 0) {
            inputRefs.current[index - 1]?.focus();
        }
    };

    const handleSubmit = (e: React.FormEvent) => {
        e.preventDefault();
        const finalCode = otp.join("");
        console.log("Submitted OTP:", finalCode);

    };

    return (
        <div className="p-4 py-10 flex overflow-hidden group m-auto justify-center maincard">
            <div className="flex flex-col gap-4 md:gap-8 items-center justify-center">
                <ScanFace className="h-8 w-8 md:h-16 md:w-16 text-[#8a8888c5] group-hover:text-[#00adef] transition-colors" />
                
                <div className="flex flex-col gap-1 items-center">
                    <h1 className="font-bold text-black text-lg md:text-xl">Authentication</h1>
                    <p className="font-light text-black text-xs md:text-sm text-center">
                        Enter the code generated by your Authentication App
                    </p>
                </div>

                <form onSubmit={handleSubmit} className="flex flex-col gap-4 md:gap-10">
                    <div className="flex gap-2 w-full items-center justify-center">
                        {otp.map((digit, idx) => (
                            <InputField
                                key={idx}
                                val={digit}
                                inputRef={(el) => (inputRefs.current[idx] = el)}
                                onChange={(val) => handleChange(val, idx)}
                                onKeyDown={(e) => handleKeyDown(e, idx)}
                            />
                        ))}
                    </div>

                    <div className="flex flex-col gap-2 items-center">
                        <button 
                            type="submit" 
                            className="w-full h-10 md:h-12 bg-[#00adef] text-sm md:text-lg 
                                     rounded-lg font-extrabold text-white p-2 hover:bg-[#008ec4] transition-colors"
                        >
                            Verify account
                        </button>
                        <p className="font-light text-black text-sm md:text-lg">
                            Didn't get the code?{' '}
                            <button type="button" className="text-black font-bold underline cursor-pointer">
                                Resend code
                            </button>
                        </p>
                    </div>
                </form>
            </div>
        </div>
    );
}